const normalize = (input: string): string =>
  input
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, ' ')
    .trim()
    .replace(/\s+/g, ' ');

const resolveSubjectKey = (raw?: string | null): string | null => {
  if (!raw) return null;
  const n = normalize(raw);
  if (!n) return null;
  if (baseNames[n]) return n;
  if (aliases[n]) return aliases[n];
  return null;
};

export const getLocalizedSubjectName = (
  rawSubject?: string | null,
  lang?: SupportedLanguage,
  fallback?: string | null,
  options?: { examCode?: string | null }
): string => {
  const key = resolveSubjectKey(rawSubject);
  if (!key) return fallback ?? rawSubject ?? '';
  const examCode = options?.examCode?.toLowerCase();
  if (lang && examCode) {
    const examSpecific = examSpecificOverrides[examCode]?.[lang]?.[key];
    if (examSpecific) return examSpecific;
  }
  const localized = lang ? localizedOverrides[lang]?.[key] : null;
  if (localized) return localized;
  return baseNames[key] ?? fallback ?? rawSubject ?? key;
};
