type SubjectProfile = Record<string, string>;
type ExamSubjectOverrides = Partial<Record<string, Partial<Record<SupportedLanguage, SubjectProfile>>>>;

const buildExamOverrides = (
  exams: string[],
  lang: SupportedLanguage,
  profile: SubjectProfile
): ExamSubjectOverrides =>
  Object.fromEntries(exams.map((examCode) => [examCode, { [lang]: profile }])) as ExamSubjectOverrides;

const mergeExamOverrides = (...sources: ExamSubjectOverrides[]): ExamSubjectOverrides =>
  sources.reduce<ExamSubjectOverrides>((acc, source) => {
    for (const [examCode, localeMap] of Object.entries(source)) {
      acc[examCode] = { ...(acc[examCode] ?? {}), ...(localeMap ?? {}) };
    }
    return acc;
  }, {});

